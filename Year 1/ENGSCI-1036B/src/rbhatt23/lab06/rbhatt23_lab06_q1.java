package rbhatt23.lab06;

public class rbhatt23_lab06_q1 {
    public static void main(String[] args) {

        // New int array with size 10
        int[] numbers = new int[10];

        // Populate it from 0-10
        for (int i = 0; i < 10; i++) {
            numbers[i] = i;
        }

        // Print the array
        for (int i = 0; i < 10; i++) {
            System.out.println(numbers[i]);
        }

    }
}
